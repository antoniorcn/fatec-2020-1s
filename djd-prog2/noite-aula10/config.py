def get_image_bygid(spritesheet, gid, columns=12, w=16, h=16, space_h=0, space_v=0, top=0, left=0):
    linha = gid // columns
    coluna = gid % columns
    x = (coluna * (w + space_h)) + left
    y = (linha * (h + space_v)) + top
    return spritesheet.subsurface((x, y), (w, h))
